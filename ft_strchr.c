char	*ft_strchr(const char *s, int c)
{
	char	*t_s;

	t_s = (char *)s;
	while(*t_s)
	{
		if(*t_s == c)
			return (t_s);
		t_s++;
	}
	if(*t_s == c)
		return (t_s);
	return (0);
}

