#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				l;
	char			*res;

	if (!s)
		return (0);
	i = 0;
	l = ft_strlen(s);
	res = (char *)malloc((l + 1) * sizeof(char));
	if (!res)
		return (0);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
